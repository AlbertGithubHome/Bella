import requests
import pickle

# resource url
target_url = 'http://www.pythonchallenge.com/pc/def/banner.p'

def main():
    file_data = requests.get(target_url).content
    data = pickle.loads(file_data)
    print ('\n'.join([''.join([p[0] * p[1] for p in row]) for row in data]))

if __name__ == '__main__':
    main()

