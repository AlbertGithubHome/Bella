from PIL import Image
import requests
import io

# resource url
target_url = 'http://www.pythonchallenge.com/pc/def/oxygen.png'

def get_image_file():
    image_data = requests.get(target_url).content
    stream = io.BytesIO(image_data)
    img = Image.open(stream)
    return img



def main():
    img = get_image_file()
    width, height = img.size
    half_height = (height + 1) // 2
    for x in range(0, width, 7):
        print(chr(img.getpixel((x, half_height))[0]), end = "")
        #print(x, img.getpixel((x, half_height)))
    numlist = [105, 110, 116, 101, 103, 114, 105, 116, 121];
    print('\nthe next level is:',''.join([chr(x) for x in numlist]))


